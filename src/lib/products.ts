export const getProductById = (id: number | string) => {
  const numId = typeof id === "string" ? parseInt(id, 10) : id;
  return PRODUCTS.find((p) => p.id === numId) || PRODUCTS[0];
};

export const getProductCategory = (p: Product): string => {
  const hay = `${p.name} ${p.details?.material ?? ""}`.toLowerCase();
  if (hay.includes("барашек")) return "barashek";
  if (hay.includes("велюр")) return "velour";
  if (hay.includes("лён") || hay.includes("лен ") || hay.includes("льна") || hay.includes("льн")) return "linen";
  if (hay.includes("муслин") || hay.includes("летний")) return "summer";
  if (hay.includes("вискоза") || hay.includes("платье")) return "dress";
  if (hay.includes("бомбер") || hay.includes("экокожа")) return "outerwear";
  if (hay.includes("футболка") || hay.includes("лонг") || hay.includes("кулирка")) return "basic-tops";
  return "other";
};

export const getRelatedProducts = (currentId: number, limit = 8): Product[] => {
  const current = PRODUCTS.find((p) => p.id === currentId);
  if (!current) return PRODUCTS.slice(0, limit);
  const cat = getProductCategory(current);
  const same = PRODUCTS.filter((p) => p.id !== currentId && getProductCategory(p) === cat);
  const others = PRODUCTS.filter((p) => p.id !== currentId && getProductCategory(p) !== cat);
  return [...same, ...others].slice(0, limit);
};
